def getImpress(url, user_agent, proxy):
    if proxy != 'L':
        proxies = {
            "http": "socks5://"+proxy+"/",
            "https": "socks5://"+proxy+"/"
        }
    else:
        proxies = False

    r = requests.get(url, allow_redirects=True, timeout=30, headers={
                     'User-Agent': user_agent}, proxies=proxies)
    return r


